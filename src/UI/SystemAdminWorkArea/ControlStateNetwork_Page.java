    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
          String name = txtFldName.getText();
          Country country = (Country) cbCountry.getSelectedItem();
        if(!name.equals("")){
                   // validate name
            String regex = "^[a-z A-Z]+$";
            Pattern namePattern = Pattern.compile(regex);
            Matcher nameMatcher = namePattern.matcher(name);

            if(!nameMatcher.matches()){
                 JOptionPane.showMessageDialog(null, "Invalid characters in state name", "Warning", JOptionPane.WARNING_MESSAGE);
                 return;
            }
            
            for(Country c: system.getNetworkList()){
               if(c.equals(country)){
                   State s = c.createState();
                    s.setName(name);
               }
              
            }
            populateTable();
            txtFldName.setText("");
            JOptionPane.showMessageDialog(null, "State added successfully", "Info", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null, "Please enter text", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

