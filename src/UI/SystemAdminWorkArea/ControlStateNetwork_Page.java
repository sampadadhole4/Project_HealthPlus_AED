    private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblState.getModel();
        dtm.setRowCount(0);
        
   
          for(Country c : system.getNetworkList()){
           
            for(State s: c.getStateList()){
               Object row[] = new Object[2];
               row[0] = c;
               row[1] = s;
               dtm.addRow(row);
            }
        }
    }
