    private void btn_goTreatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_goTreatActionPerformed
        int selectedRow = tblViewCoordinators.getSelectedRow();
        if (selectedRow >= 0) {
            SeniorCare senior = (SeniorCare) tblViewCoordinators.getValueAt(selectedRow, 0);
            String name = senior.getName();
            boolean illness = senior.isIllness();

            if(senior.isIllness() == true){
                enterprise.getSeniorCareDirectory().removeSeniorCare(senior);
                populateViewCoordinatorTable();

                SeniorMedicalWorkRequest req = new SeniorMedicalWorkRequest();
                SeniorMedical seniors = new SeniorMedical();
                seniors.setName(name);
                seniors.setIllness(illness);
                seniors.setStatus(true);
                req.setSenior(seniors);

                for(Org organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if (organization instanceof MedicalOrg) {
                        organization.getWorkQueue().getWorkRequestList().add(req);
                    }
                }

                JOptionPane.showMessageDialog(null, "Request successfully sent to Medical", "Information", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Only Seniors with illness can be sent for Treatment", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_goTreatActionPerformed


